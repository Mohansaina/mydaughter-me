export const getFeaturedProducts = () => products.filter(p => p.featured);
export const getProductsByCategory = (category: string) => products.filter(p => p.category.toLowerCase() === category.toLowerCase());
export const getProductBySlug = (slug: string) => products.find(p => p.slug === slug);
export const getRelatedProducts = (product: SimpleProduct, limit = 4) =>
    products.filter(p => p.category === product.category && p.id !== product.id).slice(0, limit);
